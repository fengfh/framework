package leap.orm.tested.model.secondary;

import leap.orm.annotation.Column;
import leap.orm.annotation.Entity;
import leap.orm.annotation.Id;
import leap.orm.model.Model;

@Entity(table = "primary_table1", secondaryTable = "secondary_table1")
public class SecondaryEntity1 extends Model {

    public SecondaryEntity1() {

    }

    public SecondaryEntity1(String index) {
        this.col1 = "c1_" + index;
        this.col2 = "c2_" + index;
    }

    @Id
    protected String id;

    @Column
    protected String col1;

    @Column(secondary = true)
    protected String col2;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCol1() {
        return col1;
    }

    public void setCol1(String col1) {
        this.col1 = col1;
    }

    public String getCol2() {
        return col2;
    }

    public void setCol2(String col2) {
        this.col2 = col2;
    }

}